def extract_source_link_from_post(soup):
    """T√¨m link g·ªëc trong ph·∫ßn #post_content c·ªßa b√†i vi·∫øt FireAnt."""
    content_div = soup.find("div", id="post_content")
    if not content_div:
        return None

    anchors = content_div.select("a[href]")
    if not anchors:
        return None

    def normalize_href(href: str) -> str:
        href = (href or "").strip()
        if not href:
            return ""
        return urljoin(FIREANT_BASE_URL, href) if href.startswith("/") else href

    def is_external(href: str) -> bool:
        try:
            u = urlparse(href)
            return bool(u.netloc) and ("fireant.vn" not in u.netloc.lower())
        except Exception:
            return False

    KEYWORDS = ("Link g·ªëc", "ngu·ªìn", "source", "xem th√™m", "ƒë·ªçc th√™m",)

    # ∆Øu ti√™n c√°c anchor c√≥ text ph√π h·ª£p, duy·ªát t·ª´ d∆∞·ªõi l√™n (cu·ªëi b√†i tr∆∞·ªõc)
    for a in reversed(anchors):
        text = (a.get_text(strip=True) or "").lower()
        href = normalize_href(a.get("href", ""))
        if any(k in text for k in KEYWORDS) and is_external(href):
            return href

    # Fallback: l·∫•y external link cu·ªëi c√πng trong ph·∫ßn n·ªôi dung
    for a in reversed(anchors):
        href = normalize_href(a.get("href", ""))
        if is_external(href):
            return href

    return None

def scroll_and_collect_links(driver, stock_code="FPT", scroll_step=500):
    url = get_stock_url(stock_code)
    driver.get(url)
    time.sleep(5)

    # Thu th·∫≠p links theo th·ª© t·ª± xu·∫•t hi·ªán t·ª´ tr√™n xu·ªëng
    links = []
    seen_links = set()  # ƒê·ªÉ track c√°c link ƒë√£ th·∫•y
    scroll_position = 0

    # ƒê·∫ßu ti√™n thu th·∫≠p links ·ªü top c·ªßa trang
    initial_articles = driver.find_elements(By.CSS_SELECTOR, "div.flex.flex-row.h-full.border-b-1 a[href^='/bai-viet/']")
    for article in initial_articles:
        try:
            href = article.get_attribute("href")
            if href and href.startswith("https://fireant.vn/bai-viet/"):
                clean_href = href.split("?")[0]
                if clean_href not in seen_links:
                    links.append(clean_href)
                    seen_links.add(clean_href)
        except:
            continue

    for i in range(MAX_SCROLLS):
        print(f"üîΩ Scroll {i+1}/{MAX_SCROLLS}")
        scroll_position += scroll_step
        driver.execute_script(f"window.scrollTo(0, {scroll_position});")
        time.sleep(4)

        # Ch·ªâ thu th·∫≠p c√°c link M·ªöI xu·∫•t hi·ªán sau khi scroll
        current_articles = driver.find_elements(By.CSS_SELECTOR, "div.flex.flex-row.h-full.border-b-1 a[href^='/bai-viet/']")
        new_links_found = 0
        
        for article in current_articles:
            try:
                href = article.get_attribute("href")
                if href and href.startswith("https://fireant.vn/bai-viet/"):
                    clean_href = href.split("?")[0]
                    if clean_href not in seen_links:
                        links.append(clean_href)
                        seen_links.add(clean_href)
                        new_links_found += 1
            except:
                continue
        
        if new_links_found > 0:
            print(f"üì∞ T√¨m th·∫•y {new_links_found} b√†i m·ªõi sau scroll {i+1}")

        # Ki·ªÉm tra xem ƒë√£ scroll h·∫øt ch∆∞a
        if scroll_position >= driver.execute_script("return document.body.scrollHeight"):
            print("üîö ƒê√£ scroll h·∫øt trang, d·ª´ng")
            break

    print(f"‚úÖ Found {len(links)} news in order from top to bottom.")
    return links

def extract_article(driver, url):
    try:
        driver.get(url)
        time.sleep(3)
        soup = BeautifulSoup(driver.page_source, "html.parser")

        title_tag = soup.select_one("div.mt-3.mb-5.text-3xl.font-semibold.leading-10")
        title = title_tag.get_text(strip=True) if title_tag else ""

        fuzzy_time = ""
        dt = None

        time_tag = soup.select_one("time[datetime]")
        if time_tag:
            fuzzy_time = time_tag.get_text(strip=True)
            try:
                raw_iso = time_tag.get("datetime") or time_tag.get("title")
                if raw_iso:
                    dt = parser.parse(raw_iso)
            except Exception as e:
                print(f"‚ö†Ô∏è L·ªói parse ISO datetime: {e}")

        if not dt:
            fuzzy_tags = soup.select("span.text-gray-500")
            if fuzzy_tags:
                for tag in fuzzy_tags:
                    parts = tag.get_text(strip=True).split("|")
                    if len(parts) >= 1:
                        fuzzy_time = parts[-1].strip()  # L·∫•y ph·∫ßn cu·ªëi (th·ªùi gian)

        content_div = soup.find("div", id="post_content")
        content = ""
        if content_div:
            paragraphs = content_div.find_all("p")
            content = "\n".join(p.get_text(strip=True) for p in paragraphs if p.get_text(strip=True))

        # üîó NEW: l·∫•y link g·ªëc (n·∫øu c√≥)
        source_link = extract_source_link_from_post(soup)

        try:
            ai_button = WebDriverWait(driver, 5).until(
                EC.element_to_be_clickable((By.XPATH, "//button[contains(., 'T√≥m t·∫Øt tin t·ª©c b·∫±ng AI')]"))
            )
            ai_button.click()
            time.sleep(10)
            soup = BeautifulSoup(driver.page_source, "html.parser")
            ai_summary_tag = soup.select_one("div.italic:not(.font-bold)")
            ai_summary = ai_summary_tag.get_text(strip=True) if ai_summary_tag else ""
        except Exception as e:
            print(f"‚ö†Ô∏è AI summary l·ªói: {e}")
            ai_summary = ""

        return {
            "title": title,
            "content": content,
            "link": url,
            "ai_summary": ai_summary,
            "fuzzy_time": fuzzy_time,
            "source_link": source_link,  # ‚úÖ NEW
        }
    except Exception as e:
        print(f"‚ùå L·ªói khi crawl b√†i vi·∫øt: {url} ({e})")
        return {}

