def crawl_fireant(stock_code="FPT", table_name="FPT_News", db_manager=None):
    """Crawl FireAnt v·ªõi logic t·ªëi ∆∞u - ch·ªâ check duplicate t·ª´ DB"""
    start_time = time.time()
    
    # T·∫°o db_manager n·∫øu kh√¥ng ƒë∆∞·ª£c truy·ªÅn v√†o
    if db_manager is None:
        db_manager = get_database_manager()
    
    print("\n===============================================================================")
    print(f"üîç ƒêang ki·ªÉm tra database cho stock: {stock_code}")
    existing_links = get_recent_links_from_db(db_manager, table_name, 100)
    print(f"üìä T√¨m th·∫•y {len(existing_links)} b√†i vi·∫øt trong DB (100 b√†i g·∫ßn nh·∫•t)")

    driver = setup_driver()
    article_links = scroll_and_collect_links(driver, stock_code=stock_code)
    
    # Ch·ªâ l·ªçc b·ªè nh·ªØng link ƒë√£ c√≥ trong DB ƒë·ªÉ tr√°nh crawl tr√πng
    links_to_crawl = [link for link in article_links if link not in existing_links]
    
    if len(links_to_crawl) > 0:
        print(f"üéØ Crawl {len(links_to_crawl)} b√†i vi·∫øt m·ªõi (b·ªè qua {len(article_links) - len(links_to_crawl)} b√†i ƒë√£ c√≥)")
    else:
        print(f"üì∞ No new news - t·∫•t c·∫£ {len(article_links)} b√†i ƒë·ªÅu ƒë√£ c√≥ trong DB")

    current_year = 2025
    new_articles = 0
    crawled_count = 0
    duplicate_count = 0
    
    for idx, link in enumerate(links_to_crawl):
        try:
            print(f"ÔøΩ [{idx+1}/{len(links_to_crawl)}] {link}")
            raw_data = extract_article(driver, link)

            dt = parse_fuzzy_datetime(raw_data.get("fuzzy_time", ""), current_year)
            raw_data["date"] = format_datetime_obj(dt) if dt else ""

            success = insert_to_supabase(db_manager, table_name, raw_data)
            if success:
                new_articles += 1
                print(f"‚úÖ ƒê√£ l∆∞u b√†i vi·∫øt: {raw_data.get('title', '')[:50]}...")
            else:
                duplicate_count += 1
                # Ch·ªâ hi·ªÉn th·ªã 3 duplicate ƒë·∫ßu ti√™n ƒë·ªÉ tr√°nh spam
                if duplicate_count <= 3:
                    print(f"‚ö†Ô∏è  Duplicate title - skipped: {raw_data.get('title', '')[:50]}...")
                elif duplicate_count == 4:
                    print(f"‚ö†Ô∏è  ... v√† {len(links_to_crawl) - idx - 1} duplicates kh√°c (kh√¥ng hi·ªÉn th·ªã)")
            
            crawled_count += 1
            time.sleep(1)  # Delay between requests
            
        except Exception as e:
            print(f"‚ùå L·ªói l·∫•y b√†i {link}: {e}")
            continue
    
    driver.quit()
    
    # T√≠nh to√°n k·∫øt qu·∫£
    end_time = time.time()
    duration = end_time - start_time
    
    return {
        'stock_code': stock_code,
        'duration': duration,
        'total_found': len(article_links),
        'crawled_count': crawled_count,
        'new_articles': new_articles,
        'stopped_early': False
    }

